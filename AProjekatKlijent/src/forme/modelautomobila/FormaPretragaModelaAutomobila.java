    private void popuniKombo() {
        komboPolje.removeAllItems();
        komboPolje.addItem("Proizvođač");
        komboPolje.addItem("Naziv");
        komboPolje.addItem("Godina nastanka");
        komboPolje.addItem("Tip modela");
    }

    private void srediTabelu() throws ServerDownException {
        ModelTabeleMA mt = new ModelTabeleMA();
        tblModeliAutomobila.setModel(mt);

        tblModeliAutomobila.getColumnModel().removeColumn(tblModeliAutomobila.getColumnModel().getColumn(5));
    }

    private void obrisiFilter() {
        ModelTabeleMA mtabele = (ModelTabeleMA) tblModeliAutomobila.getModel();

        komboPolje.getModel().setSelectedItem("Proizvođač");
        txtVrednostPretrage.setText("");
        mtabele.prikaziModeleAutomobilaNaOsnovuPretrage("", (String) komboPolje.getSelectedItem());
    }

    private void serverZaustavljen() {
        this.dispose();
        GlavnaForma fr = (GlavnaForma) this.getParent();
        fr.serverZaustavljen();
    }
