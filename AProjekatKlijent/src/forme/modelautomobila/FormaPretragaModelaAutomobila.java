    private void btnIzbrisiMAActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnIzbrisiMAActionPerformed
        int red = tblModeliAutomobila.getSelectedRow();
        if (red == -1) {
            JOptionPane.showMessageDialog(this, "Niste izabrali nijedan model automobila!");
            return;
        }
        ModelTabeleMA mtma = (ModelTabeleMA) tblModeliAutomobila.getModel();

        ModelAutomobila model = mtma.vratiModelAutomobilaURedu(red);
        try {
            String poruka = kontroler.Kontroler.getInstance().izbrisiModelAutomobila(model);
            JOptionPane.showMessageDialog(this, poruka, "Uspešno brisanje", JOptionPane.INFORMATION_MESSAGE);
            mtma.setModeleAutomobila(kontroler.Kontroler.getInstance().vratiModeleAutomobila());
        } catch (ServerDownException se) {
            JOptionPane.showMessageDialog(this, se.getMessage(), "Kraj rada", JOptionPane.WARNING_MESSAGE);
            this.dispose();
            GlavnaForma fr = (GlavnaForma) this.getParent();
            fr.serverZaustavljen();
            return;
        } catch (Exception exception) {
            JOptionPane.showMessageDialog(this, exception.getMessage(), "Neuspešno brisanje", JOptionPane.ERROR_MESSAGE);
            exception.getMessage();
        }
    }//GEN-LAST:event_btnIzbrisiMAActionPerformed

    private void btnPrikaziMAActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnPrikaziMAActionPerformed
        try {
            if (postojiIzabraniModel()) {
                JDialog formaPrikaz = new FormaModelAutomobila(null, true, FormMode.VIEW);
                JOptionPane.showMessageDialog(this, "Odabrani model automobila je pronađen!");
                formaPrikaz.setAlwaysOnTop(true);
                formaPrikaz.setLocationRelativeTo(null);
                formaPrikaz.pack();
                formaPrikaz.setVisible(true);
            } else {
                JOptionPane.showMessageDialog(this, "Sistem ne može da nađe podatke o modelu!",
                        "Neuspešna pretraga", JOptionPane.ERROR_MESSAGE);
            }
        } catch (ServerDownException se) {
            JOptionPane.showMessageDialog(this, se.getMessage(), "Kraj rada", JOptionPane.WARNING_MESSAGE);
            serverZaustavljen();
        }
    }//GEN-LAST:event_btnPrikaziMAActionPerformed
    private boolean postojiIzabraniModel() {
        if (tblModeliAutomobila.getSelectedRow() == -1) {
            return false;
        }
        return true;
    }
