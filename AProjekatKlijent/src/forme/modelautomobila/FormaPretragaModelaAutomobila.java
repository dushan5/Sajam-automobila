    private void btnPretraziMAActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnPretraziMAActionPerformed
        ModelTabeleMA mt = (ModelTabeleMA) tblModeliAutomobila.getModel();

        boolean uspesno = mt.prikaziModeleAutomobilaNaOsnovuPretrage(txtVrednostPretrage.getText(), (String) komboPolje.getSelectedItem());
        if (uspesno) {
            JOptionPane.showMessageDialog(this, "Pretraga je završena!");
        } else {
            JOptionPane.showMessageDialog(this, "Sistem ne može da nađe modele po zadatoj vrednosti!", "Neuspešna pretraga", JOptionPane.ERROR_MESSAGE);
            obrisiFilter();
        }
    }//GEN-LAST:event_btnPretraziMAActionPerformed

