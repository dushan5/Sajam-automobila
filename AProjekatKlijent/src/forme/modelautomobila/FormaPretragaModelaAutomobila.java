    private void tblModeliAutomobilaMouseReleased(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblModeliAutomobilaMouseReleased

        int red = tblModeliAutomobila.getSelectedRow();
        if (red == -1) {
            Sesija.getInstance().getUseCaseParams().put("izabraniMA", null);
            return;
        }
        ModelTabeleMA mtma = (ModelTabeleMA) tblModeliAutomobila.getModel();

        ModelAutomobila ma = mtma.vratimodelURedu(red);
        Sesija.getInstance().getUseCaseParams().put("izabraniMA", ma);
    }//GEN-LAST:event_tblModeliAutomobilaMouseReleased

